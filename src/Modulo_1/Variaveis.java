package Modulo_1;

public class Variaveis {
	public static void main(String[]args) {
		int idade = 24;
		System.out.println("minha idade é:  " + idade);
		Double altura = 70.2;
		System.out.println("minha altura  é:  " + altura);
		String nome = "Lucas";
		System.out.println("meu nome é :  " + nome);
		float peso = 72.2f;
		System.out.println("meu peso é:  " + peso);
		char letraDeUmNomeQueConheciBemOntem = 'm';
		System.out.println("essa letra querida que conheci ontem:  " +  letraDeUmNomeQueConheciBemOntem);
	}
}
