package Modulo_1;

public class Switch_e_Case {

	public static void main(String[] args) {
		int vida = 100;
		
		switch(vida) {
			case 90:
				System.out.println("vida 90");
				break;
			
			case 80:
				System.out.println("vida 80");
				break;
				
			case 70:
				System.out.println("vida 70");
				break;
			
			case 40:
				System.out.println("vida 40");
				break;
				
			case 10:
				System.out.println("vida 10");
				break;
				
			default:
				System.out.println("sem vida");
		
		}
}
}
